#Data Types

#Integer - Whole number without decimal value 
num1 = 22

#Floats - Number with decimal parts
num2 = 13.22

#String - Textual data enclosed within double quotes
msg = "Hello World!"

#Boolean - Represent true and false.
is_valid = True

print(num1, "\n")
print(num2, "\n")
print(msg, "\n")
print(is_valid, "\n")