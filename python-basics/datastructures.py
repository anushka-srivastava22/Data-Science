# Data Structures - Used for storing collection of data

#Lists -  Ordered and Mutable Collections
my_list = [1,2,3,4]

#Tuple - Ordered and Immutable Collections
my_tuple = (1,2,3)

#Dictionary - Immutable; Unordered Collections of Key Value Pair
my_dict = {'name':'Anu', 'age':23}

#Set - Unordered Collections of unique elements and is mutable
my_set = {1,2,3}

print(my_list, "\n")
print(my_tuple, "\n")
print(my_dict, "\n")
print(my_set, "\n")